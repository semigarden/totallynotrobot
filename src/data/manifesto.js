export const buildManifestoModel = (segments, layers = {}) => {
    const wordMeta = [];
    const echoLayers = Object.fromEntries(
        Object.keys(layers).map((id) => [id, ""])
    );

    segments.forEach((segment) => {
        const parts = segment.text.match(/\S+\s*/g) ?? [];
        parts.forEach((part) => {
            const word = part.trim();
            wordMeta.push({
                echo: segment.echo ?? null,
                wake: !!segment.wake,
                gloss: segment.gloss ?? null,
            });

            if (segment.echo && echoLayers[segment.echo] !== undefined) {
                const layer = echoLayers[segment.echo];
                echoLayers[segment.echo] = layer ? `${layer} ${word}` : word;
            }
        });
    });

    return {
        text: segments.map((segment) => segment.text).join(""),
        wordMeta,
        echoLayers,
    };
};
